    }

    /**
     * Handle the Order "deleted" event.
     */
    public function deleted(Order $order)
    {
        $client = $order->client;

        if ($client) {
            // Update client stats
            $client->total_orders = max(0, $client->total_orders - 1);
            $client->amount_spent = max(0, $client->amount_spent - $order->total_price);
            $client->save();
        }
    }

